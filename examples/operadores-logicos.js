// texto.toUpperCase(); -> transforma o texto em maiúscula
// texto.toLowerCase(); -> transforma em minúscula

const e1 = 'C'.toUpperCase(), e2 = 'f'.toUpperCase;

if (e1 == 'C' && e2 == 'K') {
    console.log('Celsius -> Kelvin');
} else if (e1 == 'C' && e2 == 'F')  {
    console.log('Celsius -> Fahrenheit');
}

